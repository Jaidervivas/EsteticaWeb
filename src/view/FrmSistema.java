public void listarCitasEnTabla() {
    model.CitaDAO dao = new model.CitaDAO();
    java.util.List<model.Cita> lista = dao.listarCitas();
    
    // 1. Definimos los títulos de las columnas de nuevo
    String[] titulos = {"ID", "Nombre", "Teléfono", "Servicio", "Fecha", "Hora"};
    javax.swing.table.DefaultTableModel modeloTabla = new javax.swing.table.DefaultTableModel(null, titulos);
    
    // 2. Llenamos el modelo con los datos de la lista
    Object[] fila = new Object[6];
    for (int i = 0; i < lista.size(); i++) {
        fila[0] = lista.get(i).getId();
        fila[1] = lista.get(i).getNombre();
        fila[2] = lista.get(i).getTelefono();
        fila[3] = lista.get(i).getServicio();
        fila[4] = lista.get(i).getFecha();
        fila[5] = lista.get(i).getHora();
        modeloTabla.addRow(fila);
    }
    
    // 3. ¡ESTA ES LA LÍNEA MÁS IMPORTANTE! 
    // Le dice a la tabla visual que use este nuevo modelo con datos.
    tablaCitas.setModel(modeloTabla); 
}
