    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      // Dentro del evento del botón Eliminar:
int fila = tablaCitas.getSelectedRow(); 

    if (fila == -1) {
        javax.swing.JOptionPane.showMessageDialog(null, "Por favor, seleccione una cita de la tabla.");
    } else {
        int id = Integer.parseInt(tablaCitas.getValueAt(fila, 0).toString());

        int pregunta = javax.swing.JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar esta cita?");
        
        if (pregunta == 0) { 
            model.CitaDAO dao = new model.CitaDAO();
            if (dao.eliminarCita(id)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Cita eliminada correctamente.");
                
                // Esto es para que la fila desaparezca de la vista de inmediato
                listarCitasEnTabla(); 
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Error al intentar eliminar de la base de datos.");
            }
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed
       // Creamos los objetos necesarios
model.Cita cita = new model.Cita();
model.CitaDAO dao = new model.CitaDAO();

// 1. Validamos que los campos no estén vacíos
// 1. Validamos usando los cuadros de texto (txt)
if (!txtNombre.getText().equals("") && !txtTelefono.getText().equals("") && !txtFecha.getText().equals("")) {
    
    // 2. Pasamos la información de los cuadros de texto al objeto cita
    cita.setNombre(txtNombre.getText());
    cita.setTelefono(txtTelefono.getText());
    cita.setServicio(cbxServicio.getSelectedItem().toString());
    cita.setFecha(txtFecha.getText());
    cita.setHora(txtHora.getText());
    // 3. Intentamos registrar
    if (dao.registrarCita(cita)) {
        javax.swing.JOptionPane.showMessageDialog(null, "¡Cita agendada con éxito!");
        limpiarCampos();      // Función para vaciar los cuadritos
        listarCitasEnTabla(); // Refrescamos la tabla del administrador
    } else {
        javax.swing.JOptionPane.showMessageDialog(null, "Error: El horario ya está ocupado. Elige otro.");
    }

} else {
    javax.swing.JOptionPane.showMessageDialog(null, "Por favor, llena todos los campos.");
}
    }//GEN-LAST:event_btnAgendarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int fila = tablaCitas.getSelectedRow();
    
    if (fila == -1) {
        javax.swing.JOptionPane.showMessageDialog(null, "Por favor, seleccione una cita de la tabla para modificar.");
    } else {
        // 1. Capturamos los datos de los cuadros de texto
        model.Cita cita = new model.Cita();
        
        // Sacamos el ID de la fila seleccionada (columna 0)
        int id = Integer.parseInt(tablaCitas.getValueAt(fila, 0).toString());
        
        cita.setId(id);
        cita.setNombre(txtNombre.getText());
        cita.setTelefono(txtTelefono.getText());
        cita.setServicio(cbxServicio.getSelectedItem().toString());
        cita.setFecha(txtFecha.getText());
        cita.setHora(txtHora.getText());

        // 2. Llamamos al DAO para actualizar la base de datos
        model.CitaDAO dao = new model.CitaDAO();
        if (dao.modificarCita(cita)) {
            javax.swing.JOptionPane.showMessageDialog(null, "¡Cita actualizada correctamente!");
            listarCitasEnTabla(); // Para ver los cambios en la tabla
            limpiarCampos();      // Para vaciar los cuadritos después de actualizar
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Error al intentar actualizar los datos.");
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

